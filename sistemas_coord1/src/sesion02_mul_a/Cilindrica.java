package sesion02_mul_a;


public class Cilindrica {
    
    private float alto, radio;

    public Cilindrica(float alto, float radio) {
        this.alto = alto;
        this.radio = radio;
    }
    
    public Cilindrica() {
    }

    public float getAlto() {
        return alto;
    }

    public void setAlto(float alto) {
        this.alto = alto;
    }

    public float getRadio() {
        return radio;
    }

    public void setRadio(float radio) {
        this.radio = radio;
    }
    
    
    
}
